    public boolean getCheckUsername() throws ClassNotFoundException, SQLException {
        ArrayList<String> alUsername = new ArrayList<String>();
        boolean checkUsername = true;
        Connection conn = Connectionbuilder.connect();
        String sql = "select * from APP.postuser";
        Statement std = conn.createStatement();
        std.executeQuery(sql);
        ResultSet rs = std.getResultSet();

        while (rs.next()) {
            alUsername.add(rs.getString("username"));
        }
        for (int i = 0; i < alUsername.size(); i++) {
            if(this.usernameTextField.getText().equals(alUsername.get(i))) {
                checkUsername = false;
            }
        }

        return checkUsername;
    }

