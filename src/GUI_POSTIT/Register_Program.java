    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection con = Connectionbuilder.connect();

            if (this.usernameTextField.getText().equalsIgnoreCase("") && this.nameTextField.getText().equalsIgnoreCase("")
                    && this.jPasswordField1.getText().equalsIgnoreCase("")
                    && this.jPasswordField2.getText().equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(null, "Please, Enter info");
            } else {
                if (getCheckUsername()) {
                    if (this.jPasswordField1.getText().equalsIgnoreCase(this.jPasswordField2.getText())) {

                        PreparedStatement std = con.prepareStatement("insert into APP.postuser(username,userpassword,name,userstatus) "
                                + "values(?,?,?,?)");
                        std.setString(1, this.usernameTextField.getText());
                        std.setString(2, this.jPasswordField1.getText());
                        std.setString(3, this.nameTextField.getText());
                        std.setString(4, "User");
//                        std.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Register Complete");
                        this.setVisible(false);
                        Login_Program loginPanel = new Login_Program();
                        loginPanel.setVisible(true);
                        loginPanel.setLocationRelativeTo(null);
                    } else {
                        JOptionPane.showMessageDialog(null, "Password and Re-Password are same");
                    }
                }
                else {
                    JOptionPane.showMessageDialog(null,"This username have already.");
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException err) {
            err.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

